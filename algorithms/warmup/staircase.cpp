#include <cmath>
#include <cstdio>
#include <vector>
#include <iostream>
#include <algorithm>
#include <iomanip>
using namespace std;

int main() {
	/* Enter your code here. Read input from STDIN. Print output to STDOUT */
	int num;
	cin >> num;
	for (int i = 0; i < num; i++) {
		for (int j = num-1; j >= 0; j--) {
			if (i >= j)
				cout << '#';
			else
				cout << ' ';
		}
		cout << endl;
	}

	return 0;
}

